order by total_revenue desc;

-- ? Business Problem:- The effectiveness of subscription programs is unknown.

-- Impact:- 
		-- 1.Validates subscription model performance.
    -- 2.Improves customer loyality programs.
    -- 3.Increases customer lifetime value (CLV).
	

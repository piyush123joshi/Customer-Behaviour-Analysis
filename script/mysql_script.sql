-- 10. What are the top 3 most purchased products within each category?

with cte as (
select category,item_purchased,
count(item_purchased) as most_purchased,
rank() over(partition by category order by count(item_purchased) desc) as rnk
from customer_behaviour_analysis
group by category,item_purchased
)
select * from cte
where rnk < 3;


-- ? Business Problem:- The company doesn't know top performing products within categories.

-- Impact:-
		-- 1.Improves product placement and recommendation
    -- 2.Helps inventory optimization
    -- 3.Increases sales through best seller
        
-------------------------------------------------------------------------------------------------

-- 11. Are customers who are repeat buyers (more than 5 previous purchases) also likely to subscribe?

select case
when previous_purchases > 5 then 'Repeat Buyers'
else 'Normal Buyers'
end as customer_type,
subscription_status,
count(*) as customer_count,
count(*) * 100 / sum(count(*))
over(partition by  case
when previous_purchases > 5 then 'Repeat Buyers'
else 'Normal Buyers' end) as percent
from customer_behaviour_analysis
group by 
case
when previous_purchases > 5 then 'Repeat Buyers'
else 'Normal Buyers'
end,subscription_status;

-- ? Business Problem:- Unclear relationship between loyality and subscription.

-- Impact:-
		-- 1.Improves subscription targetting
    -- 2.Increases conversion to paid program
    -- 3.Increase customer retention strategy
	

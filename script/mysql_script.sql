-- 8. Top 5 products with highest discount usage %.

select item_purchased,
count(item_purchased) as total_item_sold,
count(case when discount_applied='Yes' then 1 end) as item_sold_after_discount,
count(case when discount_applied='Yes' then 1 end) * 100 / count(*) as discount_percent
from customer_behaviour_analysis
group by item_purchased
order by discount_percent desc
limit 5;

-- ? Business Problem:-Some products may be overly dependent on discount.

-- Impact:-
		-- 1.Identifies discount-driven products.
    -- 2.Helps optimizing pricing strategy.
    -- 3.Reduces profit margin loss.
        

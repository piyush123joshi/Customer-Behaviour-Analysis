-- 9. Segement customer into new,returning and loyal based on their total number of previos purchase,
-- and show the count of each segment.

select 
case when previous_purchases='0' then 'new_customer'
	 when previous_purchases between 1 and 15 then 'returning_customer'
     else 'loyal_customer' end as customer_segment,
count(*) as customer_count
from customer_behaviour_analysis
group by case 
when previous_purchases='0' then 'new_customer'
when previous_purchases between 1 and 15 then 'returning_customer'
else 'loyal_customer'
end;

-- ? Business Problem:- Lack of customer segmentation leads to generic strategies.

-- Impact:-
		-- 1.Enables personalized marketing
    -- 2.Improves retention strategies
    -- 3.Increases conversion rates.
	
